/// Standard corner radiuses for the application.
///
/// - [sm]: Small elements like small buttons or tags (4px).
/// - [md]: Medium elements like standard buttons or inputs (8px).
/// - [lg]: Large elements like cards or bottom sheets (16px).
/// - [xl]: Extra large elements (24px).
/// - [full]: Fully rounded for pills or circular avatars (999px).
class AppRadius {
  const AppRadius._();

  static const double sm = 4;
  static const double md = 8;
  static const double lg = 16;
  static const double xl = 24;
  static const double full = 999;
}
